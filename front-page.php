      </a>
      <div class="reveal" style="display:none;">
        <p>We’ve got a big campsite right next to the main festival area, none of the hours of walking that you have to do at big festivals! The camping area is big enough for everyone at the festival to camp together. All you need to do is bring your tent. Please bear in mind we can’t have campervans or campfires at the festival.</p>

      </div>
