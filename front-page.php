      Bentley Wildfowl &amp; Motor Museum,
      Bentley, Halland,
      East Sussex,
      BN8 5AF</p>
      <p><strong>Liftshare</strong>: Check out our liftshare page to share the price of the journey. Probably the cheapest and most eco-friendly way to get there: <a href="https://liftshare.com/uk/lifts/brainchild">www.liftshare.com/uk/lifts/brainchild</a></p>
      <p><strong>Getting here by train</strong>: Nearest stations are Uckfield and Lewes. Uckfield is closer to site than Lewes, although both stations are the same distance from London (roughly 70 minutes). Brighton to Lewes is 15 minutes.     </p>
      <p><strong>From The Station:</strong></p>
      <p><strong>Minibuses</strong>: We are running a limited minibus service from Uckfield to the festival site. There will be two busses every hour from 12:30 to 20:30 on Friday, and 11:30 to 13:30 on Saturday. The price is £3 for a seat.</p>
      <p>Tickets for these are available online, we recommend buying in advance so you are guaranteed a seat on the bus when your train arrives.</p>
      <p>Ticket link: <a href="http://minibus.brainchildfestival.co.uk">minibus.brainchildfestival.co.uk</a></p>
      <p>Minibuses will also be running back to the station on the Monday morning but you will be able to buy these on-site.</p>
      <p><strong>Taxis:</strong> Please make sure you book taxis in advance of arriving at the station. There is not always availability.</p>
      <p>R&amp;R Taxis - 01825765322 (Uckfield)</p>
      <p>TMT Taxis - 01825764646 (Uckfield)</p>
      <p>GM Taxis - 01273477567 (Lewes)</p>
      <p><strong>Getting here by car</strong>: 30 minutes from Brighton via A27 &amp; A26, 60-90 minutes from London via A22.</p>
