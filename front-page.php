        <p>Essentials: your ticket and some valid ID. Brainchild is an 18+ festival and you will need this and your ticket to get your wristband and gain entry.</p>
        <p>Please do bring: things that will help you gain from the festival, so notebooks, yoga mats, cameras, instruments for the jams…</p>
        <p>Please <strong>dont</strong> bring any glass, fireworks or illegal substances. We have a zero tolerance approach to illegal drugs and legal highs.</p>

      </div>
